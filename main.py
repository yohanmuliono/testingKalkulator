
def penambahan(a,b):
    return a + b


def pengurangan(a,b):
    return a-b


def perkalian(a,b):
    return a*b


def pembagian(a,b):
    return a/b


def main():
    print(penambahan(10,5))
    print(pengurangan(10,5))
    print(perkalian(10,5))
    print(pembagian(10,5))
    
    
if __name__ == "__main__":
    main()

































